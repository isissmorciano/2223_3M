#Determina se un numero è positivo/negativo.

#Inserire il numero
numero=int(input("Inserisci il numero che vuoi verificare se è positivo o negativo"))

#Se il numero è maggiore o uguale di zero allora è positivo altrimenti è negativo
if numero>=0:
    print("Il numero è positivo")
else:
    print("Il numero è negativo")
